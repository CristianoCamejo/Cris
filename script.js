window.changeLanguage = function(lang) {
    document.documentElement.lang = lang;

    document.querySelectorAll('[data-lang-key]').forEach(element => {
        const key = element.getAttribute('data-lang-key');
        if (translations[lang][key] !== undefined) {
            if (Array.isArray(translations[lang][key])) {
                // Trata listas (Conteúdo Programático)
                element.innerHTML = translations[lang][key].map(item => `<li>${item}</li>`).join('');
            } else {
                // Trata texto normal, incluindo tags de formatação como <b>
                element.innerHTML = translations[lang][key];
            }
        }
    });
}

document.addEventListener('DOMContentLoaded', () => {
    // Inicializa o idioma para Português
    changeLanguage('pt');

    // Lógica do menu responsivo (Hamburguer)
    const menuToggle = document.getElementById('menu-toggle');
    const menuClose = document.getElementById('menu-close');
    const mainNav = document.getElementById('main-nav');

    const toggleMenu = () => {
        mainNav.classList.toggle('open');
    };

    menuToggle.addEventListener('click', toggleMenu);
    menuClose.addEventListener('click', toggleMenu);

    // Fecha o menu ao clicar em um link
    mainNav.querySelectorAll('a').forEach(link => {
        link.addEventListener('click', () => {
            if (mainNav.classList.contains('open')) {
                mainNav.classList.remove('open');
            }
        });
    });
});

